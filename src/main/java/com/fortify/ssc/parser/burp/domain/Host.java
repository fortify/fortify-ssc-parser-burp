package com.fortify.ssc.parser.burp.domain;

import com.fasterxml.jackson.dataformat.xml.annotation.JacksonXmlProperty;
import com.fasterxml.jackson.dataformat.xml.annotation.JacksonXmlText;

import lombok.Data;

/**
 * This class can be used to access both element attribute (ip)
 * and attribute value (host name/uri). Note that this is provided as
 * an example only; {@link Issue} maps the XML 'host' element to String,
 * ignoring the 'ip' attribute as this plugin only uses the host value.  
 * @author Ruud Senden
 *
 */
@Data
public class Host {
	@JacksonXmlProperty(isAttribute = true) private String ip;
	@JacksonXmlText private String uri;
}
